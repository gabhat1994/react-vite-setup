const ImageIcons = {
  avatar_m,
  leave_quietly,
  raise_hand_m,
  low_connection_xs,
};

export interface IconProps {
  name?: keyof typeof Icons;
  imageIconName?: keyof typeof ImageIcons;
  size: number;
  className?: string;
  color?: string;
  isImageIcon?: boolean;
  onClick?: HTMLAttributes<HTMLDivElement>['onClick'];
  style?: CSSProperties;
  isPinned?: boolean;
  height?: string | null;
  width?: string | null;
}

export default ({
  name,
  size,
  color,
  imageIconName,
  onClick,
  width = null,
  height = null,
  ...rest
}: IconProps) => {
  const Component = name ? Icons[name] : undefined;

  return (
    <Container
      onClick={onClick}
      height={height}
      width={width}
      size={size}
      color={color}
      {...rest}
    >
      {imageIconName ? (
        <ImageIcon
          src={ImageIcons[imageIconName]}
          alt={imageIconName}
          size={size}
        />
      ) : Component ? (
        <Component
          data-testid="svgIcon"
          className="icon"
          height={height ?? size}
          width={width ?? size}
          fill={color}
        />
      ) : null}
    </Container>
  );
};
